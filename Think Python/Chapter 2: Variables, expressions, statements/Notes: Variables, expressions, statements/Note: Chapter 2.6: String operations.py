# Addition has commutative property; string concatenation doesn't
ex1,ex2 = 2+3,3+2
print("Commutative addition examples:", "\n",
      ex1, "\n",
      ex2)

ex3,ex4 = "word_one" + " " + "word_two", "word_two" + " " + "word_one"
print("Commutative string examples:", "\n",
      ex3, "\n",
      ex4)